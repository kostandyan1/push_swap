#include "push_swap.h"

int	is_sorted(t_list **stack)
{
	t_list	*temp;

	temp = *stack;
	if (!temp)
		return (1);
	while (temp && temp->next)
	{
		if (temp->value > temp->next->value)
			return (0);
		temp = temp->next;
	}
	return (1);
}
